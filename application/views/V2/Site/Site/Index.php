    <h1>Iceland Oceanic Shuttle</h1>
    </br><div align="center"><img src="http://rts.vatsim-uk.co.uk/upload/events/463.png" /></div></br>
    <h5>When:</h5> 2014-04-06 :: 16:00-20:00</h5>
<h5>What: </h5>
6th April between 16-20z Vatsim-Scandinavia and VATSIM UK are continuing the last success of the Iceland Oceanic Shuttle Event and we are now proud to announce the 2nd edition of this event!
<br /> 
<br />This time along with Keflavik(BIKF), Bergen (ENBR) and Edinbrugh (EGPH) will be staffed for your flights in and out of Iceland!
<br /> 
<br />Make sure you attend this event as this is an unusual occasion where the whole Iceland FIR and OCA will be opened up for business. So whether you are flying from the Nordic countries, from Europe or even from North America it will surely be an interesting and exciting evening for everyone!
<br /> 
<br />Since all flights to/from Iceland are being subject to oceanic clearances/procedures please make sure that you are familiar with the procedures used in the OCA.</br>
